package Fundamentos;

public class Aritmeticos {
	public static void main(String[] args) {
		double x = 8.2;
		double y = 24.6;
		
		System.out.println(x + y);
		System.out.println(x - y);
		System.out.println(x * y);
		System.out.println(y / x);

		int a = 2;
		int b = 18;
		System.out.println(a + b);
		System.out.println(b - a);
		System.out.println(a * b);
		System.out.println(b / a);
		System.out.println(b % a);
	}
}
