package Fundamentos;

public class OperadoresRelacionais {
	public static void main(String[] args) {
		
		System.out.println(7 > 10);
		System.out.println(7 == 7);
		System.out.println(8 < 12);
		System.out.println(8 <= 10);
		System.out.println(7 >= 10);
		System.out.println(7 != 10);
	}
}
