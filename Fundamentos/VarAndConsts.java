package Fundamentos;

public class VarAndConsts {
	public static void main(String[] args) {
		// "final" significa que a variavel se torna uma constante;
		// Convenção utilizar constantes com letra maiuscula;
		final double PI = 3.14;
		double x = 3.1;
		
		double result = PI * x;
		System.out.println(result);
	};
}
